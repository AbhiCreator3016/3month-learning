# Creating variables
name = "Rahul"              # String (text)
age = 22                    # Integer (whole number)
height = 5.9                # Float (decimal number)
is_student = True           # Boolean (True/False)

# Print variables
print(name)                 # Output: Rahul
print("Age:", age)          # Output: Age: 22
print("Height:", height)    # Output: Height: 5.9

# Rules for naming variables in Python

# 1. Variable name must start with a letter or underscore (_)
# Valid: name, _age
# Invalid: 1name

# 2. Variable name can contain letters, numbers, and underscores
# Valid: student1, total_marks
# Invalid: user-name, total@marks

# 3. Variable names cannot be Python keywords
# Invalid: if, for, while, class, True

# 4. Variable names are case-sensitive
# Example: age, Age, and AGE are different variables

# 5. Spaces are not allowed in variable names
# Invalid: user name
# Valid: user_name

# 6. Use meaningful variable names (best practice)
# Good: total_price
# Bad: tp